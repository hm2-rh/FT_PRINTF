char	*ft_strncpy(char *dest, const char *src, int n)
{
	int j;

	j = 0;
	while (j < n && src[j])
	{
		dest[j] = src[j];
		j++;
	}
	while (j < n)
	{
		dest[j] = '\0';
		j++;
	}
	return (dest);
}
